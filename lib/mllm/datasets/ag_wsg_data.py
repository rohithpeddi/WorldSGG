    def get_final_data_lite(self, video_id: str) -> Dict[str, Any]:
        final_pkl = self.bbox_3d_gt_obb_final_root_dir / f"{video_id[:-4]}.pkl"
        if not final_pkl.exists():
            raise FileNotFoundError(f"Final PKL not found: {final_pkl}")

        with open(final_pkl, "rb") as f:
            rec = pickle.load(f)

        frames_final = rec.get("frames_final", None)
        if frames_final is None:
            raise ValueError(f"[final-only][{video_id}] frames_final missing in {final_pkl}")

        return frames_final


    def get_video_statistics(self, video_id: str) -> Dict[str, Any]:
        video_data = self.get_final_data_lite(video_id)

        bbox_frames = video_data.get("bbox_frames", {})
        frame_stems = list(bbox_frames.keys())
        
        # Track statistics
        object_annotation_counts = {}  # label -> count of annotations
        frames_with_annotations = 0
        
        for frame_stem in frame_stems:
            frame_data = bbox_frames.get(frame_stem, {})
            
            # bbox_frames structure: {frame_stem: {"objects": [...]}}
            # Get the objects list from frame data
            if isinstance(frame_data, dict):
                objects_list = frame_data.get("objects", [])
            elif isinstance(frame_data, list):
                objects_list = frame_data
            else:
                objects_list = []
            
            if objects_list:
                frames_with_annotations += 1
                for obj in objects_list:
                    if isinstance(obj, dict):
                        label = obj.get("label", "unknown")
                    else:
                        label = "unknown"
                    
                    if label not in object_annotation_counts:
                        object_annotation_counts[label] = 0
                    object_annotation_counts[label] += 1
        
        # Sort by count descending and return as list of [label, count]
        sorted_counts = sorted(object_annotation_counts.items(), key=lambda x: -x[1])
        
        return {
            "video_id": video_id,
            "total_frames": len(video_data.get("frame_stems", [])),
            "frames_with_annotations": frames_with_annotations,
            "unique_object_count": len(object_annotation_counts),
            "object_annotation_counts": sorted_counts
        }
        

